include('db.php');

if ($_SESSION['role'] != 'farmer') {
    header('Location: login.php');
    exit();
}

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    // Add new product
    $name = $_POST['name'];
    $description = $_POST['description'];
    $price = $_POST['price'];
    $category = $_POST['category'];
    $stock = $_POST['stock'];
    $image = $_FILES['image']['name'];

    $target_dir = "images/";
    $target_file = $target_dir . basename($_FILES["image"]["name"]);
    move_uploaded_file($_FILES["image"]["tmp_name"], $target_file);

    $stmt = $conn->prepare("INSERT INTO products (farmer_id, name, description, price, category, image, stock) VALUES (?, ?, ?, ?, ?, ?, ?)");
    $stmt->bind_param("issdssis", $_SESSION['user_id'], $name, $description, $price, $category, $image, $stock);
    $stmt->execute();
}
